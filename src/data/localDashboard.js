const toIsoDate = (d) => {
  const yyyy = d.getUTCFullYear()
  const mm = String(d.getUTCMonth() + 1).padStart(2, '0')
  const dd = String(d.getUTCDate()).padStart(2, '0')
  return `${yyyy}-${mm}-${dd}`
}

function dateRange(fromIso, toIso) {
  const from = new Date(fromIso)
  const to = new Date(toIso)
  const days = []
  for (
    let d = new Date(Date.UTC(from.getUTCFullYear(), from.getUTCMonth(), from.getUTCDate()));
    d <= to;
    d.setUTCDate(d.getUTCDate() + 1)
  ) {
    days.push(toIsoDate(d))
  }
  return days
}

export async function fetchLocalDashboard({ from, to, period, userId }) {
  // build daily array for requested range
  const days = dateRange(from, to)
  const daily = []
  const aggBySymbol = {}

  for (const day of days) {
    const record = byDate[day] || {}
    const bySymbol = {}
    let totalMs = 0
    let unknownMs = 0
    for (const [k, v] of Object.entries(record)) {
      if (k === 'null' || k === 'unknown') {
        unknownMs += Number(v || 0)
        continue
      }
      // ignore falsy keys
      if (k == null || k === 'undefined') continue
      const n = Number(v || 0)
      if (!Number.isFinite(n) || n <= 0) continue
      bySymbol[k] = n
      totalMs += n
      aggBySymbol[k] = (aggBySymbol[k] || 0) + n
    }
    daily.push({ date: day, totalMs, unknownMs, bySymbol })
  }

  // top symbols across range
  const topSymbols = Object.entries(aggBySymbol)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 8)
    .map(([symbol, totalMs]) => ({ symbol, totalMs }))

  // lightweight KPIs derived from totals
  const totalMsAll = daily.reduce((s, d) => s + (d.totalMs || 0), 0)
  const totalHours = totalMsAll / 3600000
  const kpi = [
    {
      key: 'hours_on_chart',
      title: 'Hours on Chart',
      value: Number(totalHours.toFixed(1)),
      format: 'decimal',
    },
    {
      key: 'active_symbols',
      title: 'Active Symbols',
      value: Object.keys(aggBySymbol).length,
      format: 'number',
    },
    {
      key: 'top_symbol',
      title: 'Top Symbol',
      value: topSymbols[0]?.symbol ?? '—',
      format: 'string',
    },
  ]

  const summary = [
    { title: 'Total Time', value: `${Math.round(totalHours)}h`, delta: '' },
    { title: 'Symbol Count', value: String(Object.keys(aggBySymbol).length), delta: '' },
  ]

  const insights = topSymbols.slice(0, 3).map((s, idx) => ({
    key: `insight-${idx}`,
    title: `Top symbol: ${s.symbol}`,
    description: `You spent ${Math.round(s.totalMs / 3600000)} hours on ${s.symbol} during this period.`,
    score: s.totalMs,
  }))

  const envelope = {
    period: { from, to, label: period || 'Custom' },
    behavior: { daily, topSymbols },
    kpi,
    chart: { series: [] },
    summary,
    insights,
    meta: { userId, source: 'local' },
  }

  // simulate async network
  return new Promise((resolve) => setTimeout(() => resolve(envelope), 50))
}

